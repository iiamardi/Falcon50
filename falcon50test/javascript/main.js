// Funksion për të krijuar listën e produkteve
function createProductList() {
prod.forEach((product) => {
const li = document.createElement('li');
li.innerHTML = `
    <img src="${product.image}" alt="${product.name}">
    <div class="info">
        <div class="name">${product.name}</div>
        <div class="price">${product.price}</div>
    </div>
`;
// Klikimi i produktit shfaq detajet në të majtë (carousel-left)
li.addEventListener('click', () => {
    currentIndex = prod.indexOf(product); // Update currentIndex
    showProductInLeftCarousel(product);
});

productList.appendChild(li);
});

// Shfaq produktin e parë si default
showProductInLeftCarousel(prod[currentIndex]);
}

// Funksion për të ndryshuar produktin automatikisht
function autoChangeProduct() {
setInterval(() => {
currentIndex = (currentIndex + 1) % prod.length;
showProductInLeftCarousel(prod[currentIndex]);
}, 5000); // Ndryshon çdo 8 sekonda
}

// Shto event listener për klikimin në carousel-left
document.getElementById('carousel-left').addEventListener('click', () => {
const currentProduct = prod[currentIndex];
if (currentProduct) {
window.location.href = `product.html?id=${currentProduct.id}`;
}
});

// Inicializo listën e produkteve dhe carousel-in
createProductList();
